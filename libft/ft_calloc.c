#include "libft.h"

void	*ft_calloc(size_t i, size_t n)
{
	char *s;

	if (i == 0 && n == 0)
	{
		i = 1;
		n = 1;
	}
	s = malloc(i * n);
	if (s == NULL)
		return (NULL);
	ft_bzero(s, i * n);
	return (s);
}
